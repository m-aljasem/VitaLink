-- Verification script to check RLS policies are in place
-- Run this in Supabase SQL Editor to verify the policies exist

-- Check provider_links policies
SELECT 
  schemaname,
  tablename,
  policyname,
  permissive,
  roles,
  cmd,
  qual,
  with_check
FROM pg_policies 
WHERE tablename = 'provider_links'
ORDER BY policyname;

-- Check profiles policies
SELECT 
  schemaname,
  tablename,
  policyname,
  permissive,
  roles,
  cmd,
  qual,
  with_check
FROM pg_policies 
WHERE tablename = 'profiles'
ORDER BY policyname;

